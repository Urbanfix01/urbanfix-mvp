const COUNTRY_MAP_FOCUS_BY_KEY = Object.fromEntries(
  Object.entries(COUNTRY_MAP_FOCUS).map(([country, focus]) => [normalizeCountryKey(country), { country, focus }])
);

export const getCountryMapFocus = (countryName?: string | null): CountryMapFocus | null => {
  const entry = COUNTRY_MAP_FOCUS_BY_KEY[normalizeCountryKey(countryName)];
  if (!entry) return null;
  return {
    country: entry.country,
    center: [entry.focus[0], entry.focus[1]],
    zoom: entry.focus[2],
  };
};
